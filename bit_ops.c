#include <stdio.h>

// Return the nth bit of x.
// Assume 0 <= n <= 31
unsigned get_bit(unsigned x,
                 unsigned n) {
    // YOUR CODE HERE
    // Returning -1 is a placeholder (it makes
    // no sense, because get_bit only returns 
    // 0 or 1)
	x = x >> n;
	return (x&1);
}
// Set the nth bit of the value of x to v.
// Assume 0 <= n <= 31, and v is 0 or 1
void set_bit(unsigned * x,
             unsigned n,
             unsigned v) {
/*Turn the nth bit of x into 0, add the new num with v<<n.
To change the nth  bit into 0, first leave out the digits after nth digit,
and use ^ with x to set digits before and including nth digit to 0 while leaving 
the afterward digit behind.
Finally,add them togther. 
*/
	unsigned temp1 = (*x)>>n;
	//leave out the digits after n
	unsigned num1 = temp1<<n;
	//leave out the digits before and include n
	unsigned num2 = num1 ^ (*x);
	temp1 = temp1>>1;
	unsigned unit = n+1;
	//Note that shift operator cannot performed with other arithmetic operator.
	num1 = temp1<<unit;
	num1 = num1 + num2;
	v = v<<n;
	(*x) = num1+v;
}
// Flip the nth bit of the value of x.
// Assume 0 <= n <= 31
void flip_bit(unsigned * x,
              unsigned n) {
    // YOUR CODE HERE
	unsigned digit = get_bit(*x,n);
	digit = ~ digit;// Be careful, after flipping the leading bits are all 1
	digit = digit&1;//get the last bit
	set_bit(x,n,digit);
}


